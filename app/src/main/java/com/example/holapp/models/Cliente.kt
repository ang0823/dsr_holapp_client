package com.example.holapp.models

class Cliente {
    var success: Boolean
        get() {
            return success
        }
        set(value) {
            success = value
        }
    var nombre: String
        get() {
            return nombre
        }
        set(value) {
            nombre = value
        }
    var apellido_p: String
        get() {
            return apellido_p
        }
        set(value) {
            apellido_p = value
        }
    var apellido_m: String
        get() {
            return apellido_m
        }
        set(value) {
            apellido_m = value
        }
    var username: String
        get() {
            return username
        }
        set(value) {
            username = value
        }

}